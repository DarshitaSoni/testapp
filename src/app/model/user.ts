/** user class */
export class User {
  /** store name of the user */
  public name: string;
  /** store email id of the user */
  public email: string;
  /** store password of the user */
  public password: string;
  /** store contact of the user */
  public contact_no: string;
  /** store date-of-birth of the user */
  public date_of_birth: string;
  /** store address of the user */
  public address: string;
  /** store business name of the user */
  public business_name?: string;
  /** id of the user */
  public id?: number;
}
